// Función helper para obtener todas las rutas alternativas de una ruta
export function getAlternateRoutes(currentPath: string): Record<string, string> {
  const routes = routeTranslations[currentPath];
  if (!routes) {
    return { es: currentPath, en: currentPath };
  }
  return routes;
}

// Función para obtener el idioma de una ruta
export function getLanguageFromRoute(path: string): 'es' | 'en' | null {
  // Lista de rutas que son específicamente en español
  const spanishRoutes = [
    '/precios',
    '/producto',
    '/soluciones',
    '/empresa',
    '/sobre-nosotros',
    '/carreras',
    '/prensa',
    '/contacto',
    '/documentacion',
    '/tutoriales',
    '/comunidad',
    '/registro',
    '/iniciar-sesion',
    '/prueba-gratis',
    '/terminos-y-condiciones',
    '/politica-de-privacidad',
    '/politica-de-reembolsos',
    '/seguridad',
    '/centro-de-seguridad',
    '/casos-de-exito',
    '/casos-de-uso',
    '/testimonios',
    '/comparacion',
    '/recursos',
    '/edicion-comunidad'
  ];
  
  // Verificar si la ruta o alguna de sus partes está en español
  for (const route of spanishRoutes) {
    if (path.startsWith(route)) {
      return 'es';
    }
  }
  
  // Por defecto, asumimos que las rutas en inglés son el estándar
  return 'en';
}